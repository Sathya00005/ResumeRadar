export const prepareInstructions = ({
  jobTitle,
  jobDescription,
  
}: {
  jobTitle: string;
  jobDescription: string;
  
}) => {
  const isGeneralAnalysis = jobTitle === "General" || jobDescription === "General resume analysis";
  
  if (isGeneralAnalysis) {
    return `You are an expert in ATS (Applicant Tracking System) and resume analysis.
    Please analyze and rate this resume and suggest how to improve it.
    The rating can be low if the resume is bad.
    Be thorough and detailed. Don't be afraid to point out any mistakes or areas for improvement.
    If there is a lot to improve, don't hesitate to give low scores. This is to help the user to improve their resume.
    Provide general feedback that would be applicable across various job roles and industries.
    Focus on overall resume structure, formatting, content quality, and ATS compatibility.
    Provide the feedback using the following format: ${AIResponseFormat}
    Return the analysis as a JSON object, without any other text and without the backticks.
    Do not include any other text or comments.`;
  }
  
  return `You are an expert in ATS (Applicant Tracking System) and resume analysis.
  Please analyze and rate this resume and suggest how to improve it.
  The rating can be low if the resume is bad.
  Be thorough and detailed. Don't be afraid to point out any mistakes or areas for improvement.
  If there is a lot to improve, don't hesitate to give low scores. This is to help the user to improve their resume.
  Use the job description for the job user is applying to to give more detailed feedback.
  Take the job description into consideration and provide specific recommendations for this role.
  The job title is: ${jobTitle}
  The job description is: ${jobDescription}
  Provide the feedback using the following format: ${AIResponseFormat}
  Return the analysis as a JSON object, without any other text and without the backticks.
  Do not include any other text or comments.`;
}